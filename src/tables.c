const rk3576_ddr_phy_freq_row *rk3576_ddr_select_phy_freq_row(uint32_t dram_type, uint32_t target_mhz)
{
    if (dram_type == RK3576_LPDDR4 || dram_type == RK3576_LPDDR4X) {
        if (target_mhz <= 400u)
            return &rk3576_ddr_lpddr4_phy_freq_rows[0];
        if (target_mhz <= 613u)
            return &rk3576_ddr_lpddr4_phy_freq_rows[1];
        if (target_mhz <= 1066u)
            return &rk3576_ddr_lpddr4_phy_freq_rows[2];
        return &rk3576_ddr_lpddr4_phy_freq_rows[3];
    }

    if (dram_type == RK3576_LPDDR5) {
        if (target_mhz <= 533u)
            return &rk3576_ddr_lpddr5_phy_freq_rows[0];
        if (target_mhz <= 800u)
            return &rk3576_ddr_lpddr5_phy_freq_rows[1];
        if (target_mhz <= 1600u)
            return &rk3576_ddr_lpddr5_phy_freq_rows[2];
        if (target_mhz <= 2133u)
            return &rk3576_ddr_lpddr5_phy_freq_rows[3];
        return &rk3576_ddr_lpddr5_phy_freq_rows[4];
    }

    return 0;
}

int rk3576_ddr_select_lpddr5_wck(uint32_t target_mhz, const rk3576_ddr_lp5_wck_entry **entry_out, int *low_table_out)
{
    const rk3576_ddr_lp5_wck_entry *table;
    size_t count;
    uint32_t target_x2 = 2u * target_mhz;

    if (target_mhz <= 1600u) {
        table = rk3576_ddr_lpddr5_wck_low_table;
        count = ARRAY_LEN(rk3576_ddr_lpddr5_wck_low_table);
        if (low_table_out)
            *low_table_out = 1;
    } else {
        table = rk3576_ddr_lpddr5_wck_high_table;
        count = ARRAY_LEN(rk3576_ddr_lpddr5_wck_high_table);
        if (low_table_out)
            *low_table_out = 0;
    }

    for (size_t i = 0; i < count; ++i) {
        if (target_x2 <= table[i].max_target_mhz_x2) {
            if (entry_out)
                *entry_out = &table[i];
            return 0;
        }
    }

    if (entry_out)
        *entry_out = 0;
    return -1;
}

uint32_t rk3576_ddr_pack_lpddr5_wck(const rk3576_ddr_lp5_wck_entry *entry)
{
    return (entry->byte_18_value << 24) | (entry->nibble_1c_value << 28) | (entry->byte_08_value << 8) |
           (entry->byte_10_value << 16) | entry->byte_00_value;
}

size_t rk3576_ddr_copy_valid_pctl_init_patches(rk3576_ddr_reg_patch *entries, size_t capacity)
{
    size_t count = 0;

    for (size_t i = 0; i < ARRAY_LEN(rk3576_ddr_pctl_init_patch_table); ++i) {
        const rk3576_ddr_reg_patch *entry = &rk3576_ddr_pctl_init_patch_table[i];

        if (entry->register_offset > RK3576_PCTL_INIT_PATCH_VALID_MAX_OFFSET)
            continue;
        if (entries && count < capacity)
            entries[count] = *entry;
        ++count;
    }

    return count;
}
